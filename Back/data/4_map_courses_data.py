def convert_persian_numbers_to_english(persian_number):
    persian_to_english = {
        "۰": "0", "۱": "1", "۲": "2", "۳": "3", "۴": "4",
        "۵": "5", "۶": "6", "۷": "7", "۸": "8", "۹": "9",
    }
    return ''.join(persian_to_english.get(ch, ch) for ch in persian_number)

def map_to_course(course_data):
    mapped_data = []

    for data in course_data:
        courseName = data["course_name"]

        try:
            course = AllCourses.objects.get(courseName=courseName)
            class_time1 = convert_persian_numbers_to_english(data["class_time1"].strip())
            class_time2 = convert_persian_numbers_to_english(data["class_time2"].strip())
            exam_time = convert_persian_numbers_to_english(data["exam_time"].strip())

            # Debugging: Log processed times
            print(f"Class Times for {courseName}: {class_time1}, {class_time2}")

            # Parse class_location assuming it's a time range
            location_time = convert_persian_numbers_to_english(data["class_location"])
            start_time, end_time, _ = location_time.split("-")

            # Parse exam_time
            exam_time_parts = exam_time.split(" ساعت : ")
            exam_date = exam_time_parts[0].strip()
            exam_date = exam_date.replace("/", "-")
            exam_start_end = exam_time_parts[1].split("-")
            mapped_data.append(
                {
                    "course": course,
                    "teacherName": data["instructor"],
                    "isExperimental": False,
                    "class_time1": class_time1,
                    "class_time2": class_time2,
                    "class_start_time": start_time,
                    "class_end_time": end_time,
                    "exam_date": exam_date.strip(),
                    "exam_start_time": exam_start_end[0].strip(),
                    "exam_end_time": exam_start_end[1].strip(),
                    "capacity": data["capacity"],
                    "registered": 0,
                }
            )
        except AllCourses.DoesNotExist:
            print(f"Course with name {courseName} not found")
        except ValueError as e:
            print(f"Error parsing times for course {courseName}: {e}")
    return mapped_data


